void main()
{
	char a = 1, b ;
		// 00000001(aの値1)        10110111        00000000        00000000
		// 10110111        00000000        00000000        10000000(bの値だが、不定値)
	float c , d = 1.0 ;
		// 00000000        00000000        00000000        00000000(cの値だが、不定値) 
		// 00000000        00000000        10000000        00111111(dの値1)

	c = (float) a ;
		// 00000000        00000000        10000000        00111111(cの値1だが、aのbitと違う
	b = (char) d ;
		// 00000001(bの値1, この後ろのアドレスは無視)        00000000        00000000        10000000

	b = a;
}
